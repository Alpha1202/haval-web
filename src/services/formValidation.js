export const regex = {
    empty: /^(?!\s*$)[a-zA-Z0-9.+\s,'-]+$/,
    email: /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/,
    password: /^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9])(?=.*[\W_]+)(?=.{8,})/,
    number: /^[0-9]{1,}$/,
    phoneNumber: /^[0-9]{1,13}$/,
    cf: /^[A-Za-z]{6}[0-9]{2}[A-Za-z][0-9]{2}[A-Za-z][0-9]{3}[A-Za-z]$/i,
    date: /^([0-2][0-9]|(3)[0-1])(\/)(((0)[0-9])|((1)[0-2]))(\/)\d{4}$/,
    hour: /^(2[0-3]|[01]?[0-9]):([0-5]?[0-9])$/,
    year: /^([1][9][\d]{2}|[2][0][\d]{2})$/,
    anything: /.*/
  }